messi = int(input("pick a number"))
ronaldo = int(input("pick a number"))
operator = input("pick an operation")
if operator == "multiplication": 
    print (messi * ronaldo)
if operator == "division":
    print (messi / ronaldo)
if operator == "addition":
    print (messi + ronaldo)
if operator == "subtraction":
    print (messi - ronaldo)
if operator == "exponent":
    print (messi ** ronaldo)